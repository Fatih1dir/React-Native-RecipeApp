import React,{useState,useEffect} from "react";
import { SafeAreaView, Text, FlatList, View, Image, TouchableOpacity,TextInput } from "react-native";
import styles from "./Recipes.style";
import {SEARCH_RECIPE,API_KEY} from "@env";
import { Ionicons } from "@expo/vector-icons";
import axios from "axios";


const apiKey = "&addRecipeInformation=true&number=5&";


function Recipes({navigation}) {

  const Item = ({ title, imgSource, onPress, itemData }) => {
    return (
      <TouchableOpacity style={styles.item} onPress={onPress}>
        <Text style={styles.title}>{title}</Text>
        <Image
          style={styles.logo}
          source={{
            uri: imgSource,
          }}
        />
      </TouchableOpacity>
    );
  };

  const [searchTerm, setSearchTerm] = useState("");
  const [isSearchButtonPressed, setIsSearchButtonPressed] = useState(false);

  const handleChange = (text) => {
    setSearchTerm(text);
  };

  

  const [data, setData] = useState([]);
  useEffect(() => {
    if (isSearchButtonPressed) {
      fetchData();
    }
  }, [isSearchButtonPressed]);

  const onSearchButtonPress = () => {
    setIsSearchButtonPressed(true);
  };

  const fetchData = async () => {
    try {
      const { data: ingredientdata } = await axios.get(
        SEARCH_RECIPE + searchTerm + apiKey +API_KEY
      );
      setData(ingredientdata);
    } catch (error) {
      console.error(error);
    } finally {
      setIsSearchButtonPressed(false);
    }
  };

  function onPressRecipe(ItemId){
    navigation.navigate('RecipeInfo',{ItemId: ItemId});
  };

  const parsedData = JSON.parse(JSON.stringify(data));
  const results = parsedData.results;

  return (
    <SafeAreaView style={styles.container}>
      <View style={styles.searchBarContainer}>
        <TextInput
          value={searchTerm}
          onChangeText={handleChange}
          placeholder="Search a Recipe..."
        />
        <Ionicons name="search" size={20} onPress={onSearchButtonPress}></Ionicons>
      </View>
      <FlatList
        data={results}
        renderItem={({item}) => (
          <Item title={item.title} imgSource={item.image} onPress={()=>onPressRecipe(item.id)} itemData={item}/>
        )}
        keyExtractor={(item) => item.id}
      />
    </SafeAreaView>
  );
}

